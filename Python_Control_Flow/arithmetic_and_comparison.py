#!/usr/local/bin/python3
# This script demonstrates some the use if some operators

# {i, ii} Using Arithmetic Operators
x = int('5')
y = int('6')
# For '+' 
Sum = x + y
print(f'{Sum}')

# For '-'
Subtraction = x - y
print(f'{Subtraction}')

# For '*'
Product = x * y
print(f'{Product}')

# For '/'
Division = x / y
print(f'{Division}')

# For '//'
No_Remainder = x // y
print(f'{No_Remainder}')

# For '**'
Exponent = x ** y
print(f'{Exponent}')

# For modulo
Modulo = x % y
print(f'{Modulo}') 


#{iii} Using Comparison Operators

# For 'equal to'
Equal = x == y
print(f'{Equal}')

# For 'not equal to'
Not_Equal = x != y
print(f'{Not_Equal}')

# For 'greater than'
G_T = x > y
print(f'{G_T}')

# For 'less than'
L_T = x < y
print(f'{L_T}')

# For 'greater than or equal to'
G_E = x >= y
print(f'{G_E}')

# For 'less than or equal to'
L_E =  x <= y 
print(f'{L_E}')